package arrays_collections;

public class Student {

	
	 int id;
	 String name;
	 int marks;
	 
	  Student( int id, String name, int marks)
	  {
		  this.id=id;
		  this.name= name;
		  this. marks = marks;
		  
		  
	  }
	  
	  public String toString() {
		  
		  return this.name +" "+ this .id+" "+this.marks;
	  }
	  
	  
	 
}
